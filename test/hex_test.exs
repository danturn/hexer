defmodule Hexer.HexTest do
  alias Hexer.Hex
  use ExUnit.Case, async: true

  test "Can print binary as Hex" do
    assert "0x04, 0x40, 0xFF, 0x00" == Hex.print <<4,64,255,0>>
  end

  test "Splits every 16 numbers" do
    assert "0x04, 0x40, 0xFF, 0x00, 0x01, 0x02, 0x03, 0x05, 0x06, 0x07, 0x08, 0x09, 0x08, 0x07, 0x06, 0x05\n0x04, 0x03, 0x02, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x08, 0x07, 0x06, 0x05\n0x04, 0x03, 0x02, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09" == Hex.print <<4,64,255,0,1,2,3,5,6,7,8,9,8,7,6,5,4,3,2,1,2,3,4,5,6,7,8,9,8,7,6,5,4,3,2,1,2,3,4,5,6,7,8,9>> 
  end

  test "Cannot format non binaries" do
    assert "Unable to format: [1, 2, 3] as it isn't a binary" == Hex.print([1,2,3])
  end
end
